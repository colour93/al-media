export function formatDurationClock(seconds?: number | null, fallback = '--:--'): string {
  if (seconds == null || seconds < 0 || !Number.isFinite(seconds)) return fallback;
  const total = Math.floor(seconds);
  const h = Math.floor(total / 3600);
  const m = Math.floor((total % 3600) / 60);
  const s = total % 60;
  if (h > 0) return `${h}:${m.toString().padStart(2, '0')}:${s.toString().padStart(2, '0')}`;
  return `${m}:${s.toString().padStart(2, '0')}`;
}

export function formatDurationHuman(seconds?: number | null, fallback = '0s'): string {
  if (seconds == null || seconds < 0 || !Number.isFinite(seconds)) return fallback;
  const total = Math.floor(seconds);
  const h = Math.floor(total / 3600);
  const m = Math.floor((total % 3600) / 60);
  const s = total % 60;
  const parts: string[] = [];
  if (h > 0) parts.push(`${h}h`);
  if (m > 0) parts.push(`${m}m`);
  parts.push(`${s}s`);
  return parts.join(' ');
}
